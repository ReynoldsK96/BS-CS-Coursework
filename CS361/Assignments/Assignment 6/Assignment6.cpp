#include <iostream>
#include "card.h"
#include "deck.h"
#include "hand.h"
using namespace std;

int main() {
	deck myDeck;
	hand myHand;
	// deal first card
	myHand.addCardToHand(myDeck.dealCard());
	// deal second card
	myHand.addCardToHand(myDeck.dealCard());
	// draw the hand
	myHand.drawHand();
	// output the hand's low and high values
	cout << "The hand's low  value is " << myHand.handValueLow() << endl;
	cout << "The hand's high value is " << myHand.handValueHigh() << endl;
}
